package Lote_1;
/*
 * Calcule e mostre o quadrado dos números entre 10 e 150.
 */
public class lt01_ex31 {
	
	public static void main (String args []) {
		
		for (int x=10;x<150;x++) {
			System.out.println("O quadrado de "+Integer.toString(x)+" é "+Integer.toString(x*x));
		}
		
	}
	
}
